// Эталонный стек владельца (docs/context-line.md): хвост — обвязка СКД
// (ОбщийМодуль.КомпоновкаДанных.Модуль : ... : ПроцессорВывода.Вывести(...)),
// виновник — Отчет.* выше по стеку; ожидаемая значимая строка — САМАЯ
// ГЛУБОКАЯ строка Отчет.* (МодульМенеджера : 280).
var refCulprit = "Отчет.МотивацияАдминистраторовСОтсмотром.МодульМенеджера : 280 : КомпоновкаДанных.СформироватьОтчет(...)"

var refStackLines = []string{
	"ОбщийМодуль.ДлительныеОперации.Модуль : 1390 : ВызватьПроцедуру(...)",
	"ОбщийМодуль.ДлительныеОперации.Модуль : 1546 : ВыполнитьПроцедуру(...)",
	"1 : ВариантыОтчетов.СформироватьОтчетВФоне(...)",
	"ОбщийМодуль.ВариантыОтчетов.Модуль : 2231 : СформироватьОтчет(...)",
	"Отчет.МотивацияАдминистраторовСОтсмотром.МодульОбъекта : 175 : ...",
	"Отчет.МотивацияАдминистраторовСОтсмотром.МодульМенеджера : 53 : ...",
	refCulprit,
	"ОбщийМодуль.КомпоновкаДанных.Модуль : 210 : СформироватьОтчетТаблицаЗначений(...)",
	"ОбщийМодуль.КомпоновкаДанных.Модуль : 438 : ПроцессорВывода.Вывести(ПроцессорКомпоновкиДанных, Истина);",
}

// TestContextLineSKDReference — эталонный стек владельца, LF и CRLF.
func TestContextLineSKDReference(t *testing.T) {
	lf := strings.Join(refStackLines, "\n")
	if got := contextLine(lf, true); got != refCulprit {
		t.Errorf("LF-стек: contextLine = %q, want %q", got, refCulprit)
	}
	crlf := strings.Join(refStackLines, "\r\n")
	if got := contextLine(crlf, true); got != refCulprit {
		t.Errorf("CRLF-стек: contextLine = %q, want %q", got, refCulprit)
	}
	// Опция выключена → старое правило байт-в-байт (последняя непустая строка)
	if got, want := contextLine(lf, false), lastNonEmptyLine(lf); got != want {
		t.Errorf("опция off: %q, want %q", got, want)
	}
	if got, want := contextLine(crlf, false), lastNonEmptyLine(crlf); got != want {
		t.Errorf("опция off (CRLF): %q, want %q", got, want)
	}
}

