// TestContextLineSKDTrailingNoise — CRLF-хвосты и пустые строки после
// триггерной не мешают правилу (значимая строка — последняя НЕПУСТАЯ).
func TestContextLineSKDTrailingNoise(t *testing.T) {
	ctx := "Отчет.Продажи.МодульМенеджера : 280 : КомпоновкаДанных.СформироватьОтчет(...)\r\n" +
		"ОбщийМодуль.КомпоновкаДанных.Модуль : 438 : ПроцессорВывода.Вывести(П, Истина);\r\n\r\n"
	want := "Отчет.Продажи.МодульМенеджера : 280 : КомпоновкаДанных.СформироватьОтчет(...)"
	if got := contextLine(ctx, true); got != want {
		t.Errorf("contextLine = %q, want %q", got, want)
	}
}

// TestBuildRichContextSKDSmart — проводка опции через RowBuilder: включённая
// опция меняет ТОЛЬКО context_line; context и context_hash идентичны при
// обоих значениях (непрерывность группировок agg_context).
func TestBuildRichContextSKDSmart(t *testing.T) {
	stack := "Отчет.Продажи.МодульМенеджера : 280 : КомпоновкаДанных.СформироватьОтчет(...)\r\n" +
		"ОбщийМодуль.КомпоновкаДанных.Модуль : 438 : ПроцессорВывода.Вывести(П, Истина);"
	ev := []byte("06:58.904004-1500,CALL,2,Usr=Иванов,Context='" + stack + "'\r\n")

	build := func(smart bool) *RichExt {
		b := NewRowBuilder(true, true, smart)
		f, ok := parser.ParseEventFields(ev)
		if !ok {
			t.Fatal("событие отброшено")
		}
		return b.Build(f, "2025-11-30T16:", "25113016.log", `CallsDiag\rphost_1\25113016.log`).Rich
	}
	on, off := build(true), build(false)

	if on.ContextLine != "Отчет.Продажи.МодульМенеджера : 280 : КомпоновкаДанных.СформироватьОтчет(...)" {
		t.Errorf("smart on: ContextLine = %q", on.ContextLine)
	}
	if off.ContextLine != "ОбщийМодуль.КомпоновкаДанных.Модуль : 438 : ПроцессорВывода.Вывести(П, Истина);" {
		t.Errorf("smart off: ContextLine = %q", off.ContextLine)
	}
	if on.Context != stack || off.Context != stack {
		t.Errorf("context обязан оставаться полным стеком: on=%q off=%q", on.Context, off.Context)
	}
	wantHash := city.CH64([]byte(stack))
	if on.ContextHash != wantHash || off.ContextHash != wantHash {
		t.Errorf("context_hash обязан быть хэшем ПОЛНОГО стека при любом значении опции: on=%d off=%d want=%d",
			on.ContextHash, off.ContextHash, wantHash)
	}
	// bench-схема не считает context_line вовсе — ctxSmart не активируется
	bb := NewRowBuilder(false, false, true)
	f, _ := parser.ParseEventFields(ev)
	if r := bb.Build(f, "2025-11-30T16:", "a.log", "a"); r.Rich != nil {
		t.Error("bench-режим не должен строить RichExt")
	}
}
